function buildFilename(title: string, composition: string, suffix: string, ext: string): string {
  const raw = title ||
    composition.split("\n").find(l => /^#?\s*TITLE:/i.test(l))?.replace(/^#?\s*TITLE:/i, "").trim() ||
    "hiphop-rnb-forge";
  const slug = raw
    .toLowerCase()
    .replace(/[^a-z0-9\s-]/g, "")
    .replace(/\s+/g, "-")
    .replace(/-+/g, "-")
    .trim()
    .slice(0, 80) || "hiphop-rnb-forge";
  return `${slug}${suffix}.${ext}`;
}

function sanitizeFilename(value: string): string {
  return value
    .replace(/[<>:"/\\|?*\u0000-\u001F]/g, "")
    .replace(/\s+/g, " ")
    .trim()
    .slice(0, 120) || "hiphop-rnb-forge";
}

function escapeHtml(value: string): string {
  return value
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;");
}

function preserveInlineFormatting(value: string): string {
  return escapeHtml(value)
    .replace(/\*\*(.+?)\*\*/g, "<strong>$1</strong>")
    .replace(/\*(.+?)\*/g, "<em>$1</em>");
}

function formatContent(content: string): string {
  return content
    .split("\n")
    .map((line) => {
      const trimmed = line.trim();

      if (!trimmed || trimmed === "---" || trimmed === "***") {
        return `<div style="height:6px"></div>`;
      }

      if (/^\[.+\]$/.test(trimmed)) {
        return `<div style="font-size:9px;font-weight:600;color:#A855F7;letter-spacing:0.12em;text-transform:uppercase;margin-top:16px;margin-bottom:6px;padding-bottom:4px;border-bottom:1px solid #242428;">${preserveInlineFormatting(
          trimmed.replace(/^\[/, "").replace(/\]$/, "")
        )}</div>`;
      }

      if (/^(TITLE|STYLE|FEEL|KEY|FORM|MOOD|TEMPO|LANGUAGE|MODE|INSTRUMENTS|THEME):/i.test(trimmed)) {
        return `<div style="font-family:'Courier New',monospace;font-size:9px;color:#8A8578;line-height:1.6;margin-bottom:3px;white-space:pre-wrap;">${preserveInlineFormatting(
          trimmed
        )}</div>`;
      }

      if (/^(SECTION\s+[A-Z0-9-]+\s*-|SECTION\s+[A-Z0-9-]+$)/i.test(trimmed)) {
        return `<div style="font-size:11px;font-weight:700;color:#F0EDE6;margin-top:14px;margin-bottom:4px;letter-spacing:0.04em;">${preserveInlineFormatting(
          trimmed
        )}</div>`;
      }

      if (/^(Composition|Lyrics|Arrangement)$/i.test(trimmed)) {
        return `<div style="font-size:14px;font-weight:700;color:#F0EDE6;margin-top:18px;margin-bottom:8px;letter-spacing:0.03em;">${preserveInlineFormatting(
          trimmed
        )}</div>`;
      }

      if (/^RefineCopy$/i.test(trimmed)) {
        return `<div style="font-size:9px;color:#4A4840;letter-spacing:0.08em;text-transform:uppercase;margin-bottom:8px;">${preserveInlineFormatting(
          trimmed
        )}</div>`;
      }

      if (/^(Riff|Drums|Melody|Hook|Build|Atmosphere|Direction|Notes|Arrangement lift|Tension device):/i.test(trimmed)) {
        return `<div style="font-size:10px;font-weight:600;color:#C084FC;margin-top:10px;margin-bottom:3px;">${preserveInlineFormatting(
          trimmed
        )}</div>`;
      }

      if (trimmed.startsWith("#")) {
        return `<div style="font-size:12px;font-weight:700;color:#F0EDE6;margin-top:10px;margin-bottom:4px;">${preserveInlineFormatting(
          trimmed.replace(/^#+\s*/, "")
        )}</div>`;
      }

      return `<div style="font-size:10px;color:#D6D1C7;line-height:1.72;margin-bottom:2px;white-space:pre-wrap;">${preserveInlineFormatting(
        trimmed.replace(/^[-*]\s/, "")
      )}</div>`;
    })
    .join("\n");
}


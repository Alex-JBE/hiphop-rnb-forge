async function loadPdfLibs() {
  const [{ jsPDF }, html2canvasModule] = await Promise.all([
    import("jspdf"),
    import("html2canvas"),
  ]);
  return { jsPDF, html2canvas: html2canvasModule.default };
}

async function renderToPDF(title: string, htmlContent: string, filename: string): Promise<void> {
  const { jsPDF, html2canvas } = await loadPdfLibs();

  const container = document.createElement("div");
  container.style.cssText = `
    position: fixed;
    left: -10000px;
    top: 0;
    width: 794px;
    background: #0D0D0F;
    color: #F0EDE6;
    font-family: Arial, sans-serif;
    font-size: 11px;
    line-height: 1.6;
    padding: 48px;
    box-sizing: border-box;
    z-index: -1;
  `;

  container.innerHTML = `
    <div style="font-size:24px;font-weight:700;color:#A855F7;margin-bottom:4px;">
      ${escapeHtml(title || "Untitled")}
    </div>
    <div style="font-size:9px;color:#4A4840;letter-spacing:0.08em;margin-bottom:20px;padding-bottom:12px;border-bottom:1px solid #A855F7;">
      Generated by Hip-Hop & R&B Forge · AI Composition Suite
    </div>
    ${htmlContent}
    <div style="margin-top:40px;padding-top:12px;border-top:1px solid #242428;font-size:8px;color:#4A4840;letter-spacing:0.06em;">
      Exported from Hip-Hop & R&B Forge
    </div>
  `;

  document.body.appendChild(container);

  try {
    await new Promise((resolve) => requestAnimationFrame(() => resolve(null)));

    const canvas = await html2canvas(container, {
      scale: 2,
      backgroundColor: "#0D0D0F",
      useCORS: true,
      logging: false,
      height: container.scrollHeight,
      windowHeight: container.scrollHeight,
      width: container.scrollWidth,
      scrollX: 0,
      scrollY: 0,
    });

    const pdf = new jsPDF({ orientation: "portrait", unit: "pt", format: "a4", compress: true });

    const pdfWidth = pdf.internal.pageSize.getWidth();
    const pdfHeight = pdf.internal.pageSize.getHeight();
    const margin = 24;
    const renderWidth = pdfWidth - margin * 2;
    const renderHeight = pdfHeight - margin * 2;
    const ratio = renderWidth / canvas.width;
    const fullHeight = canvas.height * ratio;

    let remaining = fullHeight;
    let sourceY = 0;
    const pageHeightInCanvas = renderHeight / ratio;
    let firstPage = true;

    while (remaining > 0) {
      if (!firstPage) pdf.addPage();
      firstPage = false;

      const pageCanvas = document.createElement("canvas");
      pageCanvas.width = canvas.width;
      pageCanvas.height = Math.min(pageHeightInCanvas, canvas.height - sourceY);

      const pageCtx = pageCanvas.getContext("2d");
      if (!pageCtx) throw new Error("Canvas context unavailable");

      pageCtx.drawImage(canvas, 0, sourceY, canvas.width, pageCanvas.height, 0, 0, canvas.width, pageCanvas.height);

      const pageHeight = pageCanvas.height * ratio;
      pdf.setFillColor(13, 13, 15);
      pdf.rect(0, 0, pdfWidth, pdfHeight, "F");
      pdf.addImage(pageCanvas.toDataURL("image/png"), "PNG", margin, margin, renderWidth, pageHeight);

      remaining -= renderHeight;
      sourceY += pageHeightInCanvas;
    }

    const pdfBlob = pdf.output("blob");
    const pdfUrl = URL.createObjectURL(pdfBlob);
    const pdfLink = document.createElement("a");
    pdfLink.href = pdfUrl;
    pdfLink.download = filename;
    pdfLink.style.display = "none";
    document.body.appendChild(pdfLink);
    pdfLink.click();
    document.body.removeChild(pdfLink);
    setTimeout(() => URL.revokeObjectURL(pdfUrl), 150);
  } catch (error) {
    console.error(error);
    alert("PDF export failed.");
  } finally {
    document.body.removeChild(container);
  }
}

export async function exportPDF(title: string, content: string): Promise<void> {
  const html = formatContent(content);
  await renderToPDF(title, html, `${sanitizeFilename(title || "hiphop-rnb-forge")}.pdf`);
}

export async function exportAllPDF(
  title: string,
  composition: string,
  coverResult: string,
  videoResult: string
): Promise<void> {
  const parts: string[] = [];
  if (composition) { parts.push(sectionDivider("Composition")); parts.push(formatContent(composition)); }
  if (coverResult) { parts.push(sectionDivider("Cover Art Prompts")); parts.push(formatContent(coverResult)); }
  if (videoResult) { parts.push(sectionDivider("Video Script")); parts.push(formatContent(videoResult)); }
  if (parts.length === 0) { alert("Nothing to export."); return; }
  await renderToPDF(title || "Untitled", parts.join("\n"), buildFilename(title, composition, "-full", "pdf"));
}

export function exportTXT(title: string, content: string) {
  const blob = new Blob([content], { type: "text/plain;charset=utf-8" });
  const url = URL.createObjectURL(blob);
  const a = document.createElement("a");
  a.href = url;
  a.download = `${sanitizeFilename(title || "hiphop-rnb-forge")}.txt`;
  a.click();
  URL.revokeObjectURL(url);
}

export function exportAllTXT(
  title: string,
  composition: string,
  coverResult: string,
  videoResult: string
) {
  const divider = "\n" + "═".repeat(60) + "\n";
  let text = `HIP-HOP & R&B FORGE — FULL EXPORT\nTitle: ${title || "Untitled"}\nGenerated by Hip-Hop & R&B Forge · AI Composition Suite\n`;
  text += divider;
  if (composition) {
    text += `COMPOSITION\n${"─".repeat(40)}\n${composition}\n`;
    text += divider;
  }
  if (coverResult) {
    text += `COVER ART PROMPTS\n${"─".repeat(40)}\n${coverResult}\n`;
    text += divider;
  }
  if (videoResult) {
    text += `VIDEO SCRIPT\n${"─".repeat(40)}\n${videoResult}\n`;
  }
  const blob = new Blob([text], { type: "text/plain;charset=utf-8" });
  const url = URL.createObjectURL(blob);
  const a = document.createElement("a");
  a.href = url;
  a.download = buildFilename(title, composition, "-full", "txt");
  a.click();
  URL.revokeObjectURL(url);
}

